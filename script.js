$(function() { //document ready
    
    //cached selectors
    const $form = $('form');
    const $results = $('.results');
    const $boardRequired = $('.boardRequired')

    //add event listener when form is submitted
    $form.on('submit', function(e) {
        e.preventDefault(); //prevent default refresh page when form submitted

        //store user answers
        const userBoardSelection = $('input[name=board]:checked').val();
        const userWeightSelection = $('input[name=weight]:checked').val();
        const userWindSelection = $('input[name=wind]:checked').val();

        //select specific array inside board object that reflects user's board selection
        const boardOptions = kiteboarderType[userBoardSelection];
        
        //remind user they have not selected a board type
        let boardRequired;
        if (boardOptions === undefined) {
            boardRequired = '<h2 class="required">Remember to select a Board.</h2>'
        }
        $boardRequired.html(boardRequired);

        //filter the options in the Board Type for the user's weight and wind range
        const filteredOptions = boardOptions.filter((kite) => {
            return (kite.weight === userWeightSelection && kite.wind === userWindSelection);
        });
        
        //show user's results
        let finalHTML;
        if (filteredOptions[0] === undefined) {
            finalHTML = `<h2 class="required">Make sure to select a weight and wind range.</h2>`;
        } else if (filteredOptions[0].kite === 'na') {
            finalHTML = `<h2>Sorry, looks like a good day for video games.</h2>`;
        } else {
            finalHTML = `<h2>You should ride a <span class="kiteSize">${filteredOptions[0].kite}</span>m² kite!</h2>`;
        }

        $results.html(finalHTML);
        $boardRequired.remove();
        window.scrollTo(0,document.body.scrollHeight);
        
    });

    //button that sends user to back to the top of the page
    $('button').on('click', function() {
        window.scrollTo(0,0);
    });


});